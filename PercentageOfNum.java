
class PercentageOfNum
  {
    public static void main(String args[]) 
    {
      int A,B,C,D,E,F;
      A=10;
      B=20;
      C=30;
      D=40;
      E=50;
      F=60;
     double total=(A+B+C+D+E+F);
     double per=(total/600)*100;
      System.out.println("The percentage of six numbers is "+per);
    }
  }