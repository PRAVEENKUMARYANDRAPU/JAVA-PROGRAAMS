
class AssignmentOperator
  {
    public static void main(String args[])
    {
    
      int a=77;
      System.out.println(a);  
    
      a+=2;  
      System.out.println(a);
      int b=25;
      b-=4;
      System.out.println(b); 
      b*=2;
      System.out.println(b); 
      b%=53;
      System.out.println(b);
      float c=b;
      c/=56;
      System.out.println(c);     
    }
      
    }